                <ul class= "lista">
                    <li>&numsp; Aprendizagem Baseada Em Projetos (ABP)</li>
                    <div class="metodologia">
                        <p>A aprendizagem baseada em projetos (ABP), (do inglês, project based learning (PBL)) – auxilia os alunos na construção 
                            de soluções colaborativas de desafios, a fim de desenvolver a criação e exploração de soluções, assim como um senso 
                            crítico e investigativo.
                        </p>
                        <p>Na ABP, os alunos devem buscar explorar as possíveis soluções por si mesmo, pois o professor não deve revelar todo o 
                            conteudo de ensino, para que os alunos possam alcançar os propósitos de aprendizado propostos.
                        </p>
                            1.&numsp; O professor apresenta um problema<br />
                            2.&numsp; Os alunos investigam as causas<br />
                            3.&numsp; Com base em suas vivências, os alunos elaboram um plano<br />
                            4.&numsp; Apresentam o plano<br />
                            5.&numsp; O professor fornece feedbacks dos erros e acertos durante o processo<br />
                    </div>
                    <li>&numsp; Aprendizagem Baseada Em Problemas (ABP)</li>
                    <div class="metodologia">
                        <p>Na Aprendizagem baseada em problemas, os alunos devem ser capazes de construir um aprendizado conceitual, na qual os alunos 
                            se reúne em grupos e debatem sobre um problema para encontrar soluções.
                        </p>
                        <p>A diferença entre a Aprendizagem Baseada Em Projetos é que a ABP exige que os alunos coloquem a mão na massa, já na 
                            aprendizagem baseada em problemas, os alunos focam na resolução teórica dos casos.
                        </p>
                    </p>
                    </div>
